public class logical {
    public static void main(String args[]){
        /* logical AND(&&) states that if both the  statements are  true ans is true else false
        System.out.println((3<0) && (4>0));*/

        /*logical OR(||) States that if any one statement is true ans is true and if both the statements are false than false 
        System.out.println((3<0) || (4>0));*/

        /* Logical NOT(!) states if statement is true ans is false and if statement is false ans is true*/
        System.out.println(!(4>0));

    }
    
}
